    private static final String TAG = "MainActivity";

    static final int REQUEST_IMAGE_CAPTURE = 1;
    Button btnCamera, btnStudent;
    ImageView imageView;
    String currentPhotoPath;

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);

        btnCamera = findViewById(R.id.buttonPicture);
        btnStudent = findViewById(R.id.buttonStudent);
        imageView = (ImageView) findViewById(R.id.imageCaptured);

        BitmapFactory.Options bmOptions = new BitmapFactory.Options();

        btnCamera.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                dispatchTakePictureIntent();
                galleryAddPic();
                Log.d(TAG, "onClick: currentPhotoPath : " + currentPhotoPath );
            }
        });

        Bitmap bitmap = BitmapFactory.decodeFile(currentPhotoPath, bmOptions);
        // bitmap = Bitmap.createScaledBitmap(bitmap, imageView.getWidth(), imageView.getHeight(), true);
        imageView.setImageBitmap(bitmap);
    }

